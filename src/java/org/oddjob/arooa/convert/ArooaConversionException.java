package org.oddjob.arooa.convert;


public class ArooaConversionException extends Exception {
	private static final long serialVersionUID = 20070328;

	public ArooaConversionException() {
		super();
	}

	public ArooaConversionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ArooaConversionException(String message) {
		super(message);
	}

	public ArooaConversionException(Throwable cause) {
		super(cause);
	}
		
}
